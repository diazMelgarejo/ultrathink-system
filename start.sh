#!/bin/bash
# start.sh - ultrathink v1.0 RC first-run launcher
#
# Starts:
#   :8000  Perplexity-Tools orchestrator
#   :8001  ultrathink reasoning bridge
#   :8002  Portal dashboard
#
# The PT control plane owns runtime decisions. This script only:
#   1. resolves local paths
#   2. gathers desired local environment hints
#   3. delegates bootstrap to Perplexity-Tools
#   4. applies the PT-resolved OpenClaw config locally
#   5. launches the services

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PATHS_FILE="$SCRIPT_DIR/.paths"

_discover_pt_dir() {
  local candidate
  candidate="$(cd "$SCRIPT_DIR/../perplexity-api/Perplexity-Tools" 2>/dev/null && pwd || true)"
  if [ -f "${candidate}/orchestrator/fastapi_app.py" ]; then
    echo "$candidate"
    return
  fi

  local root
  root="$(git -C "$SCRIPT_DIR" rev-parse --show-toplevel 2>/dev/null || echo "")"
  if [ -n "$root" ]; then
    local parent
    parent="$(dirname "$root")"
    for d in "$parent"/*/; do
      if [ -f "${d}orchestrator/fastapi_app.py" ]; then
        cd "$d" && pwd
        return
      fi
    done
  fi
  echo ""
}

_best_python() {
  local dir="$1"
  local venv_py="${dir}/.venv/bin/python"
  if [ -x "$venv_py" ]; then
    echo "$venv_py"
  else
    which python3 2>/dev/null || which python
  fi
}

PT_DIR=""
PT_PYTHON=""
US_PYTHON=""
if [ -f "$PATHS_FILE" ]; then
  # shellcheck source=/dev/null
  source "$PATHS_FILE"
fi

if [ -z "${PT_DIR:-}" ]; then
  PT_DIR="$(_discover_pt_dir)"
fi
if [ -z "${PT_PYTHON:-}" ]; then
  PT_PYTHON="$(_best_python "${PT_DIR:-$SCRIPT_DIR}")"
fi
if [ -z "${US_PYTHON:-}" ]; then
  US_PYTHON="$(_best_python "$SCRIPT_DIR")"
fi

if [ ! -f "$PATHS_FILE" ] || [[ "${1:-}" == "--discover" ]]; then
  cat > "$PATHS_FILE" << PATHSEOF
# .paths - auto-generated by ./start.sh on $(date '+%Y-%m-%d %H:%M:%S')
# Edit to override. See .paths.example for documentation.
# Regenerate: rm .paths && ./start.sh

PT_DIR="${PT_DIR}"
PT_PYTHON="${PT_PYTHON}"
US_PYTHON="${US_PYTHON}"
PATHSEOF
  echo "  Paths written to .paths (gitignored)"
  [[ "${1:-}" == "--discover" ]] && exit 0
fi

PT_PORT=${PT_PORT:-8000}
US_PORT=${US_PORT:-8001}
PORTAL_PORT=${PORTAL_PORT:-8002}
PORTAL_URL="http://localhost:${PORTAL_PORT}"

LOG_DIR="$SCRIPT_DIR/.logs"
STATE_DIR="$SCRIPT_DIR/.state"
PT_RUNTIME_STATE="$STATE_DIR/pt_runtime_payload.json"
mkdir -p "$LOG_DIR" "$STATE_DIR"

pid_on_port() { lsof -ti "tcp:$1" 2>/dev/null | head -1 || true; }

if [[ "${1:-}" == "--stop" ]]; then
  echo "Stopping ultrathink services..."
  for port in $PT_PORT $US_PORT $PORTAL_PORT; do
    pid=$(pid_on_port "$port")
    if [ -n "$pid" ]; then
      kill "$pid" 2>/dev/null && echo "  killed PID $pid (:$port)" || true
    else
      echo "  nothing on :$port"
    fi
  done
  exit 0
fi

if [[ "${1:-}" == "--status" ]]; then
  echo ""
  echo "=== ultrathink service status ==="
  for port in $PT_PORT $US_PORT $PORTAL_PORT; do
    pid=$(pid_on_port "$port")
    if [ -n "$pid" ]; then
      echo "  UP   :$port  (PID $pid)"
    else
      echo "  DOWN :$port"
    fi
  done
  echo ""
  exit 0
fi

_IP_VARS="$(cd "$SCRIPT_DIR" && "$US_PYTHON" -c "
import sys, io, contextlib
sys.path.insert(0, '.')
try:
    from network_autoconfig import NetworkAutoConfig
    cfg = NetworkAutoConfig()
    with contextlib.redirect_stdout(io.StringIO()):
        mac_ip = cfg.get_working_local_ip()
    win_ip = cfg.preferred_ips.get('Windows', '192.168.254.101')
    print('MAC_IP=' + mac_ip)
    print('WIN_IP=' + win_ip)
except Exception:
    print('MAC_IP=192.168.254.105')
    print('WIN_IP=192.168.254.101')
" 2>/dev/null)"

eval "$_IP_VARS"
MAC_IP="${MAC_IP:-192.168.254.105}"
WIN_IP="${WIN_IP:-192.168.254.101}"

export OLLAMA_MAC_ENDPOINT="${OLLAMA_MAC_ENDPOINT:-http://${MAC_IP}:11434}"
export OLLAMA_WINDOWS_ENDPOINT="${OLLAMA_WINDOWS_ENDPOINT:-http://${WIN_IP}:11434}"
export LM_STUDIO_MAC_ENDPOINT="${LM_STUDIO_MAC_ENDPOINT:-http://${MAC_IP}:1234}"
export LM_STUDIO_WIN_ENDPOINTS="${LM_STUDIO_WIN_ENDPOINTS:-http://${WIN_IP}:1234}"
export WINDOWS_IP="${WINDOWS_IP:-${WIN_IP}}"
export GPU_BOX="${GPU_BOX:-WINUSER@${WIN_IP}}"

echo "  IPs   Mac=${MAC_IP}  Win=${WIN_IP}"

_MODE="offline"
_DISTRIBUTED="no"

if [ -n "${PT_DIR:-}" ] && [ -f "$PT_DIR/orchestrator.py" ]; then
  echo "  PT    reconciling runtime..."
  if (cd "$PT_DIR" && \
      WINDOWS_IP="${WIN_IP}" \
      OLLAMA_MAC_ENDPOINT="http://${MAC_IP}:11434" \
      "$PT_PYTHON" orchestrator.py bootstrap \
        --non-interactive \
        --output "$PT_RUNTIME_STATE" \
        >> "$LOG_DIR/pt-bootstrap.log" 2>&1); then
    export PT_RUNTIME_STATE
    _PT_EXPORTS="$("$US_PYTHON" - "$PT_RUNTIME_STATE" << 'PYEOF'
import json, sys
payload = json.load(open(sys.argv[1], encoding="utf-8"))
print("PT_AGENTS_STATE=" + payload.get("paths", {}).get("routing_state", ""))
print("OPENCLAW_GATEWAY_URL=" + payload.get("gateway", {}).get("gateway_url", ""))
print("OPENCLAW_GATEWAY_PORT=" + str(payload.get("gateway", {}).get("gateway_port", 18789)))
print("PT_GATEWAY_READY=" + ("yes" if payload.get("gateway", {}).get("gateway_ready") else "no"))
print("PT_AUTORESEARCH_READY=" + ("yes" if payload.get("autoresearch", {}).get("ready") else "no"))
print("PT_MODE=" + payload.get("role_routing", {}).get("topology", "unknown"))
print("PT_DISTRIBUTED=" + ("yes" if payload.get("routing", {}).get("distributed") else "no"))
PYEOF
)"
    eval "$_PT_EXPORTS"
    _MODE="${PT_MODE:-unknown}"
    _DISTRIBUTED="${PT_DISTRIBUTED:-no}"
    echo "  Mode  ${_MODE}"

    echo "  OpenClaw applying PT-resolved config..."
    PT_RUNTIME_STATE="$PT_RUNTIME_STATE" \
      "$US_PYTHON" "$SCRIPT_DIR/openclaw_bootstrap.py" --apply --payload "$PT_RUNTIME_STATE" \
      >> "$LOG_DIR/openclaw-apply.log" 2>&1 \
      || { echo "  OpenClaw apply failed - see $LOG_DIR/openclaw-apply.log"; exit 1; }
  else
    echo "  PT bootstrap failed - see $LOG_DIR/pt-bootstrap.log"
    exit 1
  fi
fi

if [ "$_DISTRIBUTED" = "yes" ] \
   && [ -n "${PT_DIR:-}" ] \
   && [ -f "$PT_DIR/scripts/launch_researchers.py" ]; then
  echo "  AutoResearchers starting -> $LOG_DIR/researchers.log"
  (cd "$PT_DIR" && \
    WINDOWS_IP="${WIN_IP}" \
    OLLAMA_MAC_ENDPOINT="http://${MAC_IP}:11434" \
    PT_RUNTIME_STATE="${PT_RUNTIME_STATE}" \
    "$PT_PYTHON" scripts/launch_researchers.py \
    >> "$LOG_DIR/researchers.log" 2>&1) &
fi

wait_for_port() {
  local port=$1 label=$2 tries=0
  printf "  waiting for %s (:%s)" "$label" "$port"
  while ! nc -z localhost "$port" 2>/dev/null; do
    sleep 0.5
    tries=$((tries+1))
    printf "."
    if [ $tries -ge 150 ]; then
      echo " TIMEOUT (check .logs/$(echo "$label" | tr '[:upper:]' '[:lower:]').log)"
      return 0
    fi
  done
  echo " UP"
}

open_browser() {
  local url=$1
  if command -v open &>/dev/null; then
    open "$url"
  elif command -v xdg-open &>/dev/null; then
    xdg-open "$url"
  fi
}

echo ""
echo "=== ultrathink v1.0 RC - starting ==="
echo ""

if [ -n "$PT_DIR" ] && [ -f "$PT_DIR/orchestrator.py" ]; then
  if pid_on_port "$PT_PORT" | grep -q .; then
    echo "  PT    :$PT_PORT already running"
  else
    echo "  PT    starting -> $LOG_DIR/pt.log"
    (cd "$PT_DIR" && PYTHONPATH="$PT_DIR" "$PT_PYTHON" -m uvicorn orchestrator.fastapi_app:app \
      --host 0.0.0.0 --port "$PT_PORT" \
      >> "$LOG_DIR/pt.log" 2>&1) &
    wait_for_port "$PT_PORT" "PT"
  fi
else
  echo "  PT    skipped (not found at $PT_DIR)"
fi

if pid_on_port "$US_PORT" | grep -q .; then
  echo "  US    :$US_PORT already running"
else
  echo "  US    starting -> $LOG_DIR/us.log"
  (cd "$SCRIPT_DIR" && PYTHONPATH="$SCRIPT_DIR" PT_RUNTIME_STATE="$PT_RUNTIME_STATE" "$US_PYTHON" -m uvicorn api_server:app \
    --host 0.0.0.0 --port "$US_PORT" \
    >> "$LOG_DIR/us.log" 2>&1) &
  wait_for_port "$US_PORT" "US"
fi

if pid_on_port "$PORTAL_PORT" | grep -q .; then
  echo "  Portal :$PORTAL_PORT already running"
else
  echo "  Portal starting -> $LOG_DIR/portal.log"
  (cd "$SCRIPT_DIR" && PYTHONPATH="$SCRIPT_DIR" "$US_PYTHON" -m uvicorn portal_server:app \
    --host 0.0.0.0 --port "$PORTAL_PORT" \
    >> "$LOG_DIR/portal.log" 2>&1) &
  wait_for_port "$PORTAL_PORT" "Portal"
fi

echo ""
echo "  PT     http://localhost:${PT_PORT}/health"
echo "  US     http://localhost:${US_PORT}/health"
echo "  Portal ${PORTAL_URL}"
echo "  JSON   ${PORTAL_URL}/api/status"
echo ""

if [[ "${1:-}" != "--no-open" ]]; then
  open_browser "$PORTAL_URL"
fi

echo "Logs: $LOG_DIR/"
echo "Stop: ./start.sh --stop"
echo ""
